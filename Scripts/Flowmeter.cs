using SQLite4Unity3d;

public class Flowmeter
{

    [PrimaryKey, AutoIncrement]
    public int Id { get; set; }
    public float Instant_consumption { get; set; }
    public float Total_consumption { get; set; }
    public string Time { get; set; }
    public int Day { get; set; }
    public int Month { get; set; }
    public int Year {  get; set; }
    public string Date { get; set; }
    public override string ToString()
    {
        return string.Format("[Flowmeter: Id={0}, Instant_consumption={1}, Total_consumption={2}, Day={3}, Month={4}, Year={5}, Time={6}, ]", Id, Instant_consumption, Total_consumption, Day, Month, Year, Time);
    }
}
